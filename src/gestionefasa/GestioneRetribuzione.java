    private void AggiornaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AggiornaActionPerformed
        int Ore, Figli;
        double sti;
         if(ore.getText().isEmpty() || figli.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "I campi sono vuoti o errati", "Errore", JOptionPane.ERROR_MESSAGE);
        }else{
            int row= tabella.getSelectedRow();
            tabella.setValueAt(Integer.parseInt(ore.getText()), row, 5);
            tabella.setValueAt(Integer.parseInt(figli.getText()), row, 6);
            Ore=Integer.parseInt(ore.getText());
            Figli=Integer.parseInt(figli.getText());
            sti=Double.parseDouble(tabella.getValueAt(row, 7).toString());
            sti=sti + (Figli*100) + (Ore*5);
            tabella.setValueAt(sti, row, 7);
        }
    }//GEN-LAST:event_AggiornaActionPerformed

