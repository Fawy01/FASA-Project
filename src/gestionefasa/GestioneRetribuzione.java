    public void visualizzaTab(){
        int l=vetPersonale.size();
        int i;
        DefaultTableModel model=(DefaultTableModel) tabella.getModel();
        Object rowData[]=new Object[8];
        for(i=0; i<l;i++){
            p=(Personale) vetPersonale.get(i);
            b=(Banca) vetBanca.get(i);
            rowData[0]=p.cognome;
            rowData[1]=p.nome;
            rowData[2]=p.data;
            rowData[3]=p.codfisc;
            rowData[4]=b.iban;
            rowData[5] = p.ore;
            rowData[6]=p.figli;
            rowData[7]=p.stipendio;
            model.addRow(rowData);
        }
    }
